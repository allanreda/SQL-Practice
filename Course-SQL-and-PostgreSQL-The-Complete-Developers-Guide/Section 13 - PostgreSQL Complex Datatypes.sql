-- 130. More on Number Data Types
SELECT 2 + 2;

SELECT (2);

SELECT (2.0);

SELECT (2.0::INTEGER);

SELECT (2.0::SMALLINT);

SELECT (999999::SMALLINT);

SELECT (9999999999999999::INTEGER);

SELECT (1.99999::REAL - 1.99998::REAL);

SELECT (1.99999::DECIMAL - 1.99998::DECIMAL);

SELECT (1.99999::NUMERIC - 1.99998::NUMERIC);

-- 131. Reminder on Character Types
SELECT (12.1223342::INTEGER);

SELECT ('dfgsdfasfg'::CHAR(3));

SELECT ('a'::CHAR(5));

SELECT ('dfgsdfasfg'::VARCHAR(5));

SELECT ('a'::VARCHAR(5));

SELECT ('fdzgsdhs'::TEXT);

-- 132. Boolean Data Types
SELECT ('true'::BOOLEAN);

SELECT ('false'::BOOLEAN);

SELECT ('1'::BOOLEAN);

SELECT (1::BOOLEAN);

SELECT (0::BOOLEAN);

-- 133. Times, Dates, and Timestamps
SELECT ('NOV-20-1980'::DATE);

SELECT ('NOV 20 1980'::DATE);

SELECT ('NOV 20, 1980'::DATE);

SELECT ('1980 NOV 20'::DATE);

SELECT ('1980 November 20'::DATE);

SELECT ('01:23 PM'::TIME);

SELECT ('01:23 PM'::TIME WITHOUT TIME ZONE);

SELECT ('01:23:40 PM'::TIME WITHOUT TIME ZONE);

SELECT ('01:23:40 AM EST'::TIME WITH TIME ZONE);

SELECT ('01:23:40 AM UTC'::TIME WITH TIME ZONE);

SELECT ('NOV-20-1980 AM PST'::TIMESTAMP WITH TIME ZONE);

